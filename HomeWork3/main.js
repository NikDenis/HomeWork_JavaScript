// Задание 3
// Пользователь с клавиатуры вводит 3 числа, необходимо создать функцию, которая
// определяет максимальное значение среди этих чисел

function maxNumber(num1, num2, num3) {
  let max = num1;
  if (max < num2) max = num2;
  if (max < num3) max = num3;
  return max;
}

const number1 = prompt("Введите первое число");
const number2 = prompt("Введите второе число");
const number3 = prompt("Введите третье число");

console.log(
  `Максимальное число из трех чисел: ${number1}, ${number2}, ${number3} равно: ${maxNumber(
    number1,
    number2,
    number3
  )}`
);

