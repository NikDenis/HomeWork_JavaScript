// текст "Размер заработной платы за вычетом налогов равен значение"

function salarySize(num) {
  if (isNaN(num)) {
    alert("Значение задано неверно");
  } else {
    alert(`Размер заработной платы за вычетом налогов равен ${num * 0.87}`);
  }
}
const salary = prompt("Введите размер заработной платы");
